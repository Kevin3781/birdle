  // ── Bird card overlay ─────────────────────────────────────────────────────
  function showBirdCard({ bird, won, guessCount, mode, shareText, shareLine }) {
    const card = el('overlay-card');

    const resultEl = el('card-result');
    if (won) {
      resultEl.className = 'card-result card-result--correct';
      resultEl.textContent = guessCount === 1 ? 'Solved on the song alone!' : `Solved on clue ${guessCount} of 5`;
    } else {
      resultEl.className = 'card-result card-result--wrong';
      resultEl.textContent = 'Out of guesses — nice try!';
    }

    const cardPhoto = el('card-photo');
    cardPhoto.onerror = () => {
      if (cardPhoto.src !== bird.imageUrl && bird.imageUrl) cardPhoto.src = bird.imageUrl;
    };
    cardPhoto.src = photoUrl(bird);
    cardPhoto.alt = bird.commonName;
    el('card-name').textContent = bird.commonName;
    el('card-name').classList.toggle('card-name--win', !!won); // one-time light-catch sheen on a win
    el('card-sci').textContent = bird.scientificName;

    // Facts
    const factsList = el('card-facts');
    factsList.innerHTML = '';
    [
      { key: 'size', label: 'Size' },
      { key: 'habitat', label: 'Habitat' },
      { key: 'diet', label: 'Diet' },
      { key: 'migration', label: 'Migration' },
    ].forEach(({ key, label }) => {
      if (bird.facts?.[key]) {
        const li = document.createElement('li');
        li.innerHTML = `<strong>${label}:</strong> ${escHtml(bird.facts[key])}`;
        factsList.appendChild(li);
      }
    });
    if (bird.funFact) {
      const li = document.createElement('li');
      li.className = 'card-fun-fact';
      li.innerHTML = `<strong>Fun fact:</strong> ${escHtml(bird.funFact)}`;
      factsList.appendChild(li);
    }

    // Audio — local clip if we have one, else the Xeno-canto link (fine post-reveal)
    const audioWrap = el('card-audio');
    const audioPlayer = el('card-audio-player');
    const xenoWrap = el('card-xeno');
    const localAudio = assetUrl(media(bird.id).audio);
    if (localAudio) {
      audioPlayer.src = localAudio;
      audioWrap.hidden = false;
      xenoWrap.hidden = true;
    } else {
      audioWrap.hidden = true;
      const query = encodeURIComponent(bird.xenoCantoQuery || bird.commonName);
      el('card-xeno-link').href = `https://xeno-canto.org/explore?query=${query}`;
      xenoWrap.hidden = false;
    }

    // External links
    const linksEl = el('card-ext-links');
    linksEl.innerHTML = '';
    if (bird.ebirdUrl) {
      linksEl.innerHTML += `<a class="card-ext-link" href="${bird.ebirdUrl}" target="_blank" rel="noopener">View on eBird ↗</a>`;
    }
    if (bird.audubonUrl) {
      linksEl.innerHTML += `<a class="card-ext-link" href="${bird.audubonUrl}" target="_blank" rel="noopener">Audubon Guide ↗</a>`;
    }

    // Point-of-use photo attribution (CC-BY / CC-BY-SA compliance)
    const creditEl = el('card-credit');
    const cm = media(bird.id);
    if (cm.imageArtist) {
      const lic = cm.imageLicenseUrl
        ? `<a href="${escHtml(cm.imageLicenseUrl)}" target="_blank" rel="noopener">${escHtml(cm.imageLicense || 'license')}</a>`
        : escHtml(cm.imageLicense || '');
      const src = cm.imageSource
        ? `<a href="${escHtml(cm.imageSource)}" target="_blank" rel="noopener">Wikimedia Commons</a>`
        : 'Wikimedia Commons';
      creditEl.innerHTML = `Photo: ${escHtml(cm.imageArtist)} / ${src} (${lic})`;
      creditEl.hidden = false;
    } else {
      creditEl.hidden = true;
    }

    // Share (daily only) vs. Back-to-archive (archive only)
    const shareBlock = el('card-share');
    const btnShare = el('btn-card-share');
    const btnNext = el('btn-card-next');
    if (mode === 'daily') {
      shareBlock.hidden = false;
      el('share-preview').innerHTML = buildShareSegments(won, won ? guessCount : 0);
      el('share-line').textContent = shareLine || '';
      btnShare.hidden = false;
      btnShare.dataset.text = shareText || '';
      btnShare.onclick = () => shareResult(shareText, btnShare);
      btnNext.hidden = true;
      startCountdown();
    } else {
      shareBlock.hidden = true;
      btnShare.hidden = true;
      btnNext.hidden = false;
      stopCountdown();
    }

    card.hidden = false;
    document.body.classList.add('overlay-open');
  }

  // 5-segment row: filled up to the solve clue (win) or all-X (fail). Filled
  // segments pop in in sequence (echoing the spoke-unlock cadence).
  function buildShareSegments(won, solveClue) {
    let html = '';
    for (let i = 1; i <= 5; i++) {
      const delay = (0.55 + (i - 1) * 0.08).toFixed(2);
      if (won && i <= solveClue) html += `<span class="seg seg--filled" style="animation-delay:${delay}s"></span>`;
      else if (!won) html += '<span class="seg seg--fail">✕</span>';
      else html += '<span class="seg"></span>';
    }
    return html;
  }

  // Live countdown to the next daily bird (local midnight).
  let _cdTimer = null;
  function startCountdown() {
    const node = el('card-countdown'); if (!node) return;
    const tick = () => {
      const now = new Date();
      const next = new Date(now.getFullYear(), now.getMonth(), now.getDate() + 1, 0, 0, 0, 0);
      let s = Math.max(0, Math.floor((next - now) / 1000));
      const h = String(Math.floor(s / 3600)).padStart(2, '0');
      const m = String(Math.floor((s % 3600) / 60)).padStart(2, '0');
      s = String(s % 60).padStart(2, '0');
      node.textContent = `Next bird in ${h}:${m}:${s}`;
    };
    tick();
    clearInterval(_cdTimer);
    _cdTimer = setInterval(tick, 1000);
  }
  function stopCountdown() { clearInterval(_cdTimer); _cdTimer = null; }

  function hideBirdCard() {
    stopCountdown();
    el('overlay-card').hidden = true;
    document.body.classList.remove('overlay-open');
    const player = el('card-audio-player');
    if (player && !player.paused) player.pause();
  }

