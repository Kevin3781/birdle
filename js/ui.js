  function escHtml(str) {
    return String(str)
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;');
  }

  // ── Cursor interaction ──────────────────────────────────────────────────────
  // Tilt is applied ONLY to image spokes (silhouette/photo) — they have no
  // interactive children, so shifting their bounding box can't break clicks.
  // The hub (which holds the input + autocomplete) is never transformed; it only
  // gets a spotlight gradient via --mx/--my.
  function attachTilt(node) {
    node.addEventListener('mousemove', e => {
      const r = node.getBoundingClientRect();
      const dx = (e.clientX - r.left - r.width / 2) / (r.width / 2);
      const dy = (e.clientY - r.top - r.height / 2) / (r.height / 2);
      node.style.transition = 'transform 0.08s ease-out';
      node.style.transform = `perspective(700px) rotateX(${(-dy * 5).toFixed(2)}deg) rotateY(${(dx * 7).toFixed(2)}deg)`;
    });
    node.addEventListener('mouseleave', () => {
      node.style.transition = 'transform 0.55s cubic-bezier(0.22,1,0.36,1)';
      node.style.transform = '';
    });
  }

  (function initHubSpotlight() {
    const hub = el('game-hub');
    if (!hub) return;
    hub.addEventListener('mousemove', e => {
      const r = hub.getBoundingClientRect();
      hub.style.setProperty('--mx', `${(e.clientX - r.left).toFixed(0)}px`);
      hub.style.setProperty('--my', `${(e.clientY - r.top).toFixed(0)}px`);
    });
    hub.addEventListener('mouseleave', () => {
      hub.style.setProperty('--mx', '50%');
      hub.style.setProperty('--my', '50%');
    });
  })();

